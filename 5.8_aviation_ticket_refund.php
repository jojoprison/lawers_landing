    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Правила возврата за авиабилеты
    </h3>
    При обращении к компании-перевозчику или оформлении искового заявления в суд, клиент должен опираться на федеральное
    законодательство, Конституцию, Закон «О защите прав потребителей», Воздушный кодекс и другие нормативные акты.
    Только в последнюю очередь принимаются в расчет правила, которые перевозчик установил внутри компании.
    <br/>
    <br/>

    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Куда обращаться для получения денег
    </h3>
    Правила возвращения авиабилетов описаны во внутренних правилах каждого авиаперевозчика. Ознакомиться с ними можно на
    официальном сайте компании(ссылка ниже). Если нет возможности воспользоваться интернетом, то необходимую информацию
    можно получить по телефону справочной службы перевозчика.
    <br/>
    <br/>
    Каждая компания утверждает собственные внутренние правила. Но при всем многообразии они не могут противопоставляться
    общим правилам воздушных перевозок. В правилах должно быть указано, что при отказе совершить перелет, клиент может
    получить соответствующую сумму в кассе, агентствах, имеющих право продажи авиабилетов и в других пунктах, которые
    могут совершать такие действия.
