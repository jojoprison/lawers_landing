                <h3 style="color:green;-webkit-text-stroke: 0.2px black">
                    К кому лучше обращаться? Как выбирать брокеров?
                </h3>
                У брокера в России должна быть соответствующая лицензия на проведение брокерской деятельности
                (форекс-дилера). С 2016 года на территории РФ вступил в силу закон, согласно которому предоставлять
                услуги онлайн-трейдинга могут исключительно брокеры с лицензией Центрального Банка Российской Федерации.
                Выделим пять пунктов лицензии от ЦБ РФ:
                <br/>
                <br/>
                <ol>
                    <li>Введение нового термина «неквалифицированный инвестор». Суть его заключается в ограничении
                        доступа к бирже до удостоверения брокера в финансовой грамотности инвестора. Этот шаг, как
                        полагают специалисты, позволит сократить убытки и улучшить экономическое положение на рынке.
                    </li>
                    <li>Подписание договоров и идентификация инвесторов. Пункт обеспечивает безопасность всем участникам
                        рынков
                    </li>
                    <li>ЦБ является регулятором и надзорным органом. На случай разногласий между участниками
                        предусмотрены твердые основания для обращений.
                    </li>
                    <li>Запрет на перевод в ПАММ-счета. Маленькие пары с минимальным кредитным плечом – всё, чем могут
                        довольствоваться трейдеры.
                    </li>
                    <li>Маленькое кредитное плечо -- потенциально низкие убытки.</li>
                </ol>
