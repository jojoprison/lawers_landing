    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Фишинг от мошенников
    </h3>
    От имени Банка на электронную почту поступает письмо с просьбой подтвердить информацию или с предупреждением о
    попытке взлома. Для избежания неприятной ситуации мошенники просят перейти по ссылке, прикрепленной в письме. При
    переходе Вы попадаете на сайт схожий с сайтом вашего Банка, вводите реквизиты карты, при этом полностью уверены, что
    это ради Вашей безопасности. В итоге мошенники узнают информацию и используют ее против Вас.
