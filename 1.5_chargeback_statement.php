    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Формулировка просьбы заявления на чарджбэк
    </h3>
    Сделайте уважительное обращение к Банку, напишите в какую компанию, когда и какая сумма была отправлена.
    Приложите банковскую выписку с отмеченными транзакциями для оспаривания. Далее, без эмоций, опишите
    ситуацию. Сделайте ссылки на правила Платежных систем, чтобы Банк понял, что Вы владеете информацией, и
    у него не было соблазнов Вам сразу отказать. Как сделать чарджбэк, читайте в статье:
    /pages/1.3.php.
    <br/>
    <br/>
    В основной части заявления формулировки не ограничиваются никакими правилами, но просторечия будут
    неуместны. Старайтесь придерживаться официально-делового стиля.
    <br/>
    <br/>
    Дата подачи заявления выравнивается по левому краю, подпись рядом с датой выравнивается по правому краю.
    Подписывать обязательно вручную, даже если заявление печатали на компьютере.
    <br/>
    <br/>
    Чтобы банковский специалист не потерял часть заявления, пронумеруйте все страницы снизу. После даты
    укажите, что прикрепляете доказательную базу в Приложении.
    <br/>
    <br/>

    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Диспутные формы заявлений на чарджбэк
    </h3>
    Банки предлагают заполнять диспутную (шаблонную) форму заявлений на фирменных бланках. Причины ясны –
    снижение нагрузки претензионных отделов и ускорение обработки информации банковскими специалистами. Но
    такая форма для успешного чарджбэка недостаточна. По шаблонным формам Банк примет заявление и, вероятно,
    направит его на чарджбэк, но в результате брокер подготовит качественный ответ и тем самым усугубит
    ситуацию. Поэтому наши рекомендации:
    1) Писать диспутную форму + уточнения + приложение доказательной базы;
    2) Заявление в свободной форме (наша рекомендация).
