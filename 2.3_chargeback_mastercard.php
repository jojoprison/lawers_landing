    Код ситуации, подходящей для начала процедуры чарджбэк в системе MasterCard – 4855 (Товары и услуги не
    предоставлены). На страницах 46-47 Chargeback Guide от 13 December 2018 можно ознакомиться с деталями
    чарджбэка по MasterCard. Сроки и коды во многом похожи на визовские. По коду 4855 ограничения составляют
    120 дней с ожидаемого момента получения услуги. Блокирующий период – 540 дней с момента транзакции; по
    истечении этого срока процедура чарджбэк становится недоступной.
