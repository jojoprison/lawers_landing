    <h3>Что такое chargeback?</h3>
    Chargeback (дословно: charge — оплата, back — возврат) — процедура оспаривания транзакций, проведенных с помощью
    банковских карт, регламентируемый правилами платежных систем VISA, MasterCard, МИР (в платежной системе МИР это
    называется диспут).
    <br/><br/>
    Вот что пишет Сбербанк на своем сайте о процедуре chargeback:
    <br/>
    Иными словами, чарджбэк — это механизм защиты пользователей пластиковых карт, чтобы Вы могли расплачиваться в
    кафе, магазинах и т.д. (ТСП — торго-сервисных предприятиях) и не переживать, что Ваши деньги будут отправлены не
    по адресу, либо произойдет двойное списание, либо услуг или товар вам будут оказаны не в ожидаемом качестве.

    Вы неоднократно видели логотип VISA, MasterCarsd(Maestro), МИР на своих банковских картах — это ваша защита!
    <br/><br/>
    Участники процедуры чарджбэк: <br/>
    1) Инициатор чарджбэк (держатель карты, обманутый вкладчик) – Вы;<br/>
    2) Банк-эмитент (Банк, выпустивший Вашу карту);<br/>
    3) Банк-эквайер (Банк продавца, в данном случае, брокера);<br/>
    4) Брокер (продавец, юридическое лицо, принявшее от Вас платеж);<br/>
    5) Платежная система — сторонний наблюдатель. Подключается к процессу в крайних случаях в порядке арбитража.
