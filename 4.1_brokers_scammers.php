    <h3 style="color:green;-webkit-text-stroke: 0.2px black">Обман форекс брокеров:</h3><br/>
    <ul style="list-style-type: decimal">
        <li>Подключение к торговому аккаунту различных роботов/советников с “отличной“ статистикой.</li>
        <br/>
        <li>На самом деле данная программа в первые 7-14 дней дает отличные результаты. Это дополнительная
            мотивация для дополнительного пополнения счета, но в один момент робот делает не верный шаг и
            счет в один миг обнуляется.
        </li>
        <br/>
        <li>Доверительное управление торгового счета “опытным трейдерам“.</li>
        <br/>
        <li>С трейдерами заключается договор доверительного управления. Трейдер юридически никак не связан с
            брокером. Как правило, трейдеры это бедолаги, которые потеряли свои деньги в торговле и которым
            надо отдавать долги, они берут деньги у других людей, чтобы отыграться. Доверять такому трейдеру
            чревато потерей денег.
        </li>
        <br/>
        <li>Торговля с «аналитиками», как еще один способ обмана.</li>
        <br/>
        <li>Объясним почему аналитики в «кавычках», чтобы быть специалистом, необходимо иметь ФСФР
            сертификат, хотя бы серии 1.0(Аттестат Банка России серии 1.0 необходим для специалистов
            финансового рынка по брокерской, дилерской деятельности, по управлению ценными бумагами и
            форекс-дилера). «Аналитики» зачастую разбираются в рынках, но дают заведомо проигрышные сделки.
            Получается, что трейдеры своими руками открывают эти сделки и ответственность оставляют на себе.
        </li>
        <br/>
        <li>Самая распространенная ситуация, когда брокер не дает возможности для вывода денежных средств,
            ни заработанных ни личных.
        </li>
        <br/>
        <li>Они придумывают разные причины для этого: не работает вывод средств, необходимо заплатить
            дополнительную комиссию или налоги, после оплаты, опять появляются какие то требования, бывает
            такое, что деньги якобы отправлены Вам, но почему то не зачислены на Ваш банковский счет и в чем
            проблема они не понимают.
        </li>
        <br/>
