    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Мошенники на сайтах знакомств
    </h3>
    По времени есть разные способы развода.
    <br/>
    <br/>
    - быстрый. Начиная переписку, Ваш собеседник переводит разговор в Skype. Там начинаются заигрывания и все плавно
    переходит в интимное видео, которое записывается втайне от Вас. А после начинается шантаж: видео увидят все Ваши
    друзья в социальных сетях, если Вы не заплатите нужную сумму.
    <br/>
    <br/>
    - долгий. После длительного общения, когда отношения уже перешли в доверительные, мошенник просит решить финансовые
    проблемы: оплатить учебу, билет на поездку к Вам, операцию родителей и другие причины. Когда вторая половинка
    высылает сумму интернет мошеннику, онлайн-отношения резко заканчиваются.
