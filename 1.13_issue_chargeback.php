    <h3 style="color:green;-webkit-text-stroke: 0.2px black">ШАГ 1 Возврат денежных средств без участия
        банка</h3>
    Перед подачей заявления по оспариванию транзакций, обязательно нужно попытаться урегулировать спорный
    вопрос с продавцом и вернуть свои денежные средства напрямую. В переписке стоит упомянуть, какие
    конкретно пункты Договора были нарушены Компанией. Если решить проблему мирным путем не удалось,
    рекомендуется сделать скриншоты переписки и приступать к следующему шагу.
    <br/><br/><br/>
    <h3 style="color:green;-webkit-text-stroke: 0.2px black">ШАГ 2 Оценка шансов на успех</h3>
    Ни банк, ни юридическая компания, не могут гарантировать 100% возврат средств с помощью чарджбэка,
    однако чем больше перечисленные ниже пункты будут соответствовать Вашей ситуации, тем выше вероятность
    победы.
    <br/><br/>
    Итак, основные требования к проведению процедуры чарджбэк:
    <br/><br/>

    <ul>
        <Li>прошло не более 540 дней с последней транзакции по платежной системе VISA; MasterCard 540 дней;
            МИР 180 дней
        </Li>
        <Li>оплата услуг была осуществлена только через банковскую карту на сайте Компании т.е. не через
            мобильное приложение банка, электронные кошельки и т.п.
        </Li>
        <Li>денежные средства были зачислены на юридическое лицо, а не на физическое. Яркими примерами
            переводов на физ. лицо служат такие назначения платежа как SBOL и P2P
        </Li>
    </ul>
