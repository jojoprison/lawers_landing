    <h3 style="color:green;-webkit-text-stroke: 0.2px black">ШАГ 3 Расторжение договора</h3>
    Данный этап предполагает разрыв отношений с компанией-продавцом. Для этого необходимо изучить на сайте
    Компании Клиентское соглашение, иначе его называют договор-оферты, найти пункт регламентирующий порядок
    расторжения договора, и в свободной форме, соблюдая нормы договора, составить Расторжение.
    <br/><br/>
    В данном документы Вам необходимо указать, какие пункты Клиентского соглашения были нарушены компанией.
    Например, в договоре с брокером указано, что срок вывода составляет 7 дней при соблюдении клиентом
    определенных условий. Но не смотря на выполнение клиентом всех пунктов, вывод денежных средств так и не
    был осуществлен.
    <br/><br/>
    Помимо этого, в расторжении стоит упомянуть о том, что Вы уже пытались самостоятельно урегулировать
    данный вопрос с компанией, однако она в свою очередь решила вас проигнорировать либо вовсе, отказала.
    Далее, указывается требование о: возврате всех денежных средств на вашу карту; расторжение договорных
    отношений; предоставлении ответа в течение 7 календарных дней.
    <br/><br/>
    Готовый документ необходимо отправить на электронный адрес Компании.
    <br/><br/>
    Если в течение указанного времени компания не реагирует на обращение, можно приступать к следующему
    этапу.
