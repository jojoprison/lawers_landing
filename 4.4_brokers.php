    <h3 style="color:green;-webkit-text-stroke: 0.2px black">Слив депозита на Forex?</h3><br/>
    Самое частое несчастье, которое постигает большинство неопытных трейдеров — это слив депозита. Что это такое?
    <br/><br/>
    Если по-простому, слив депозита — это закрытие сделки, в результате которого инвестор теряет практически все
    средства со своего счета. Причины бывают разные:
    <br/>

    <ul>
        <li>несоответствие объема сделок и доступной суммы;</li>
        <li>слишком большое количество сделок, при котором трейдер не успевает среагировать на резкое движение рынка;
        </li>
        <li>технический сбой;</li>
        <li>ложная надежда на изменение цены в пользу трейдера.</li>
    </ul>

    <br/>
    В каждом из таких случаев инвестору остается винить самого себя. Однако если слив происходит по инициативе
    форекс-брокера, это является актом мошенничества. А если говорить прямым текстом — грабежом.
    <br/><br/>


    <h3 style="color:green;-webkit-text-stroke: 0.2px black">Можно ли наказать брокера по закону?</h3><br/>
    Слив депозита — это лишь один из приемов нечестного брокера нажиться на доверчивом и неопытном трейдере. Вариантов
    потерять деньги на Форекс в результате обмана и «разводов» достаточно. О каких-то из них, а также о способах
    возврата средств мы писали в предыдущих статьях. Здесь мы только повторим главное: если случилось неприятное, деньги
    можно постараться вернуть. Механизмы и способы есть. Нет только одного — гарантий. Как и во всем, что связано с
    миром Форекс.
    <br/><br/>
    Но можно ли добиться наказания брокера по закону? Однозначного ответа на этот вопрос дать нельзя. А в большинстве
    случаев он и вовсе будет отрицательным.
    <br/><br/>
    Брокерские аферы относятся к категории экономических правонарушений. Таких как хищение средств, обман потребителя,
    интернет-мошенничество. Несмотря на то, что в Уголовном кодексе РФ хватает статей для борьбы с подобными
    преступлениями, а все же судебная практика в этой сфере, особенно интернет-мошенничества, еще не достаточно развита
    и по-прежнему вызывает затруднения. Но это не самая главная причина, по которой лжеброкеры остаются безнаказанными.
    <br/><br/>
    Нечистые на руку брокерские компании делают все, чтобы обезопасить себя на случай обращения обманутых клиентов в
    правоохранительные органы. Поэтому «черные» брокеры не регистрируют свою деятельность на территории стран, под
    юрисдикцию которых не хотят попасть. Чаще всего аферисты регистрируются на офшорах, становясь недосягаемыми для
    законодательства, скажем, Российской Федерации. В суд на таких мошенников подавать совершенно бесполезно. И лучшее
    решение здесь — принять все необходимые меры, чтобы избежать обмана вовсе.
