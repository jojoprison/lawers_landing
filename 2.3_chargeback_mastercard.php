    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        MasterCard и инвестиции:
    </h3>
    Возвратные платежи доступны для операций, в которых любая стоимость приобретается для азартных игр,
    инвестиций или аналогичных целей. Однако эмитенты не имеют прав на возвратный платеж для использования
    этих фишек или стоимости, неизрасходованных фишек, или изъятия такой стоимости, или на выигрыши, прибыли
    или убытки в результате использования таких фишек или стоимости. Простыми словами, если Вашей целью было
    вложение с целью заработка (инвестиции), то MasterCard такие транзакции принимать не станет. Поэтому
    стоит доказывать, что как таковых инвестиций не было. Либо зачастую брокер блокирует кабинет, тем самым
    отказываясь выполнять услуги. При наличии достоверной доказательной базы, шансы на победу значительно
    возрастут.
