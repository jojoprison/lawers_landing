        себе будущих жертв</h3><br/>
    <b>1. Холодный обзвон.</b><br/>Большой % Клиентов привлекается методом холодного обзвона по различным
    открытым источникам, таким как: avito, юла, из рук в руки.
    Выложили объявление о продаже автомобиля? Ждите звонка.
    Помимо открытых источников, многие брокеры покупаю базы данных других идентичных мошенников, а так же
    базы банков, страховых компаний и т.п.
    Звонки совершаются с разными предлогами, например: Проводился розыгрыш и именно Ваш номер стал
    победителем и Вы получаете бесплатное обучение финансовой грамотности. Цель такого звонка привлечь
    человека в офис(офисы брокеров выглядят очень солидно и дорого) и конечно, складывается впечатление, что
    здесь можно заработать.
    Так же звонки могут поступать с предложением быстрого заработка, в этом случае работа ведется
    дистанционно, по средствам мобильной связи или скайпа.
    <br/><br/>
    <b>2. Социальные сети.</b><br/>Работает на подобии холодных звонков: в личные сообщения поздравляет
    приятная девушка(фэйк) с Дней Рождения, она представляется сотрудницей брокера и дарит сертификат на
    прохождение курсов той же финансовой грамотности. В процессе обучения Клиент теряет свои деньги.
    <br/><br/>
    <b>3. Радио эфиры.</b><br/>Многие люди не доверяют интернету, но доверяют радиостанциям. Это доверие,
    иногда, ставит в невыгодное положение, когда рекламой радиостанции пользуются мошенники. Заработок радио
    – радиоэфиры, рекламные ролики. Самые излюбленные станции брокеров Вести FM(Ранее было так) ведь там
    собрана частичка делового мира России, а это заинтересованная аудитория и толще кошелек.
    <br/><br/>
    <b>4. Торговый центр.</b><br/>Под предлогом пройти социальный опрос или поучаствовать в розыгрыше
    собираются контактные данные, далее они обзванивают, вручается тот же сертификат на обучение или
    поступает предложение заработка.
    <br/><br/>
    <b>5. Приглашение на Работу.</b><br/>Ранее этот метод являлся самым надежным. Вы в поисках работы, либо
    сами откликаетесь на заманчивые объявления с удобным графиком, высокой зарплатой или поступают звонки с
