    <h3>Как сделать чарджбэк?</h3>
    Чарджбэк мы делим на несколько этапов:<br/>
    <br/>
    1) Подготовительный этап(сбор доказательной базы);<br/>
    2) Попытки урегулирования с продавцом(не со всеми, в зависимости от добросовестности);<br/>
    3) Расторжение договорных отношений(часто Клиент даже не знает, что заключил договор Оферты);<br/>
    4) Заявление на chargeback;<br/>
    5) Решение банка(отправил эмитент заявление эквайеру, либо запросил дополнительные документы, либо
    отказал);<br/>
    6) Pre-arbitration/Second chargeback;<br/>
    7) Арбитраж.<br/> <br/> <br/>

