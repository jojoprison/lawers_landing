    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Как написать заявление на чарджбэк
    </h3>
    Заявление на чарджбэк, как любое другое заявление, можно написать от руки на листе формата А4, но для
    удобства получателя заявление лучше распечатать на принтере и подписать собственноручно.
    Требования к написанию заявлений указаны в Федеральном законе № 59 «О порядке рассмотрения обращений
    граждан Российской Федерации». Ознакомиться можно по ссылке:
    http://www.consultant.ru/document/cons_doc_LAW_59999/.
    <br/>
    <br/>
    В общепринятой практике заявление включает в себя следующие данные (Статья 7. Требования к письменному
    обращению):
    <br/>
    <br/>
    <ol>
        <li>Сведения об адресате и заявителе;</li>
        <li>Наименование документа;</li>
        <li>Формулировку просьбы или предложения;</li>
        <li>Дату подачи;</li>
        <li>Подпись.</li>
    </ol>
    <br/>
    <img src="https://charge-backer.ru/wp-content/uploads/2020/01/2-6.png" alt="заявление"
         width="100%">
    <br/>
    <br/>
    Форма заявления стандартная. Справа в верхнем углу листа указываются реквизиты адресата. В данном
    случае, получателем будет Ваш Банк, реквизиты доступны на сайте Банка. Ниже – реквизиты отправителя, то
    есть Ваши данные. Еще ниже и по центру – название документа.
    <br/>
    <br/>
    <b>Один из частых вопросов: с маленькой или большой буквы писать слово ЗАЯВЛЕНИЕ?</b>
    <br/>
    <br/>
    Существует три допустимых варианта:
    1. Со строчной буквы в начале строки и с точкой после слова (заявление.).
    Стандартный способ оформления документа.
    <br/>
    <br/>
    2. С прописной буквы без точки в конце. (Заявление)
    При написании слова «заявление» в центре листа оно становится заголовком документа. И к нему применяется
    правило: первая буква — прописная, в конце точки нет.
    <br/>
    <br/>
    3. Все буквы прописные без точки в конце. (ЗАЯВЛЕНИЕ)
    Здесь применяется то же правило, что и в предыдущем пункте. По центру — заголовок документа, значит,
    точка в конце не нужна.
    <br/>
    <br/>
    Часто можно встретить разногласия, нужно ли ставить в строке о заявителе предлог «от». То есть как
    правильно: «Васильевой Василисы» или «от Васильевой Василисы»? Допустимы оба варианта, хотя традиционная
    форма написания все же без предлога.
