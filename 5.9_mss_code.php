    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Почему особенно важно знать MCC код, если подключен cash-back?
    </h3>
    Для извлечения большей выгоды от кэшбэка, вам необходимо быть очень внимательным, когда совершаете
    покупки, так как Банки не всегда правильно присваивают МСС код предприятию.
    <br/>
    <br/>
    То есть, если эквайер случайно или осознанно присвоил ТСП неверный код (например, вместо 5812 (Места
    общественного питания, рестораны) стоит 7011 (Отели и мотели – нигде более не классифицированные), в
    этом случае клиент не получит обещанный cash-back (бонус) за категорию 5812 (Места общественного
    питания, рестораны).
    <br/>
    <br/>
    Чтобы в будущем операции отражались с верным МСС кодом, данная организация должна самостоятельно
    обратиться в свой обслуживающий банк и подать заявку на смену MCC кода.
    <br/>
    <br/>
    Не стоит забывать, что у одной торговой точки часто может быть несколько эквайеров и соответственно
    разные MCC. То есть, некоторые кассы могут проводить операцию с одним MCC, а другие будут присваивать
    совершенно другой код. Будьте внимательны!
    <br/>
    <br/>

