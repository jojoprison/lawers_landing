    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Взлом социальных сетей
    </h3>
    Зачастую в соцсети встречаются сообщения от «друзей» с просьбой занять денег на пару дней. Около 5 лет назад схема
    собирала крупные деньги, сейчас на уловку попадаются неопытные пользователи интернета.
