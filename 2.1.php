                <br/>
                <br/>
                3.1.1. Условия запуска БП Диспут
                БП Диспут предназначен для разрешения типовых диспутов, не связанных с несанкционированным
                использованием карты. Типовыми являются диспуты, существо которых описывается причиной запуска БП
                Диспут.
                Инициатором БП Диспут может быть как Эмитент, так и Эквайрер. Если инициатор БП Диспут – Эмитент,
                получателем является Эквайрер. Если инициатор БП Диспут – Эквайрер, получателем является Эмитент.
                Срок, в течение которого инициатор вправе запустить БП Диспут, определяется причиной БП Диспут.
