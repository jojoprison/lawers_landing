    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Авиакомпания банкрот
    </h3>
    Гораздо труднее получить возврат, если авиаперевозчик обанкротился. При желании можно обратиться в суд, и можно с
    уверенностью сказать, что суд будет на вашей стороне, и вы выиграете процесс. Но проблема заключается в том, что у
    компании, скорее всего, денег нет и возвращать вам деньги просто нечем.
    <br/>
    <br/>

    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Возврат денег за авиабилет из-за коронавируса.
    </h3>
    Большинство авиакомпаний выполняют возврат средств за неиспользованные билеты в виде депозита или ваучера. Депозит —
    это когда стоимость авиабилета возвращается на виртуальный счет в личном кабинете. Этим депозитом можно будет
    воспользоваться в течение года для оплаты стоимости нового билета. В условиях пандемии коронавирусной инфекции
    многие авиакомпании отказались от дополнительных сборов за отмену даже невозвратных билетов.
    <br/>
    <br/>
    Также некоторые авиакомпании позволяют неиспользованный билет конвертировать без штрафов в билет с открытой датой. В
    большинстве случаев дату вылета по таким билетам можно будет изменить один раз.
    <br/>
    <br/>
    Если Вас не устраивают данные варианты, или Ваша авиакомпания не предложила такие варианты, то используйте выше
    указанные советы.
    <br/>
    <br/>

