    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Пройди опрос - получи деньги
    </h3>
    Заманчивое предложение: за 5 минут получить кругленькую сумму. А всего-то и надо ответить на ряд простейших
    вопросов. После прохождения для перевода выигрыша Вам необходимо внести закрепительный платеж и он довольно мал от
    100 до 300 рублей. Таких денег не жалко, ведь через несколько минут они умножатся в десятки раз. Не верьте в легкий
    заработок.
