    Инвестирование в недвижимость одно из лучших примеров надежных вложений. При не большом капитале, но при большом желании можно найти со инвеститоров либо вложиться через ипотеку. Объект недвижимости устойчив к инфляции, а его стоимость в долгосрочной перспективе постоянно растет. Квартира для инвестирования не должна быть вашей основной жилплощадью, так как квартира должна стать вашим активом.
    <br/>
    <br/>
    Получить доход от недвижимости можно двумя способами:
    <br/>
    <br/>
    <ol>
        <li>
            Сдавая в аренду
        </li>
        <li>
            Перепродав ее по более высокой стоимости
        </li>
    </ol>
    Для полной уверенности вкладываться в недвижимость или нет, стоит учесть несколько факторов:
    <br/>
    <br/>
    -для чего вы приобретаете недвижимость в собственное пользование или для сдачи в аренду;
    <br/>
    <br/>
    -какое местоположение объекта, наличие транспортной коммуникаций, насколько развита инфраструктура;
    <br/>
    <br/>
    Так же не маловажный фактор покупки недвижимости на стадии строительства или же с готовыми документами. При покупке, на стадии строительства необходимо учитывать немаловажные факторы:
    <br/>
    <br/>
    – законности строительства объекта;
    <br/>
    <br/>
    – какие имеются на данном этапе строительства документы;
    <br/>
    <br/>
    – когда окончится строительство объекта;
    <br/>
    <br/>
    – имеются ли документы на подключение коммуникаций.
    <br/>
    <br/>
    Приобретение недвижимости уже с готовыми документами (вторичное жильё) это всегда удобно имеется уверенность в законности недвижимого имущества. От этого, вторичное жильё всегда дороже.
    <br/>
    <br/>

    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Минусы
    </h3>
    1) Недобросовестный застройщик-это не только потерянные денежные средства, но и судебные тяжбы с неизвестным исходом. Поэтому, если вы собрались покупать недвижимость на стадии строительства, лучше, это делать по рекомендациям. Проверив перед покупкой документы и убедившись, что в них полный порядок вы избавляетесь от риска потери своих финансов.
    <br/>
    <br/>
    2) Некоторое жильё бывает не так, то просто продать, если ваша недвижимость не пользуется спросом, то на ее перепродажу может понадобится и пару лет.
    <br/>
    <br/>
    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Плюсы
    </h3>
    1) Главное преимущество в том, что недвижимость не может полностью обесценится и ее стоимость не упадет до нуля как это может быть с ценными бумагами или валютой. Приобретение собственного жилья это всегда приятно, при наличии двух квартир одну из них всегда можно сдать в аренду и превратить свою квартиру в собственный актив который вам приносит постоянный доход.
    <br/>
    <br/>
    2) Цены на недвижимость растут и через лет 5 вашу квартиру можно продать за более высокую цену чем вы ее приобретали
    <br/>
    <br/>
    3) Любой Банк вам выдаст большую сумму кредитных средств под залог недвижимости для развития своего бизнеса. Кроме того в любой момент вы можете начать эксплуатировать недвижимость в своих личных целях.
    <br/>
    <br/>
    4) Инвестирование в недвижимость это по своей сути тоже бизнес, который не требует расходов на заработную плату, маркетинг, аренду, перевозки и т. п.
