    <h3>В каких случаях деньги не вернут: </h3>
    <ul>
        <li>Покупатель вернул деньги за товар/услуг и пробует еще вернуть по чарджбэк;</li>
        <li>Подтверждал на сайте свое согласие с ценой, но потом утверждает, что цена завышена;</li>
        <li>Сам ошибся, купил к примеру футболку другого размера и говорит, что компания отправила не тот размер;
        </li>
        <li>Клиент сам опоздал на самолет, рейс уже был;</li>
        <li>Нарушены сроки операции;</li>
        <li>Оплата была без участия пластиковой карты;</li>
        <li>Оплата произведена на электронные кошельки/криптовалюту/физ лица;</li>
        <li>Клиент пытается обмануть, получил товар и просит возврат денег.</li>
    </ul>
    <h3>В каких ситуациях используют чарджбэк:</h3>
    <ul>
        <li>Оплаченный товар не был доставлен, и продавец не хочет добровольно возвращать деньги;</li>
        <li>Оплаченная услуга не была выполнена;</li>
        <li>Доставлен товар, либо выполнена услуга, существенно отличающаяся от описанной при покупке;</li>
        <li>Мошенничество. Списание денег без ведома владельца карты;</li>
        <li>Двойное списание денежных средств со счета;</li>
        <li>Списание большей суммы в момент перевода денежных средств;</li>
        <li>Введение покупателя в заблуждение;</li>
        <li>Денежные средства не были выданы банкоматом.</li>
    </ul>


