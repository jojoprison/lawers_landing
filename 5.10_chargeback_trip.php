    Рассмотрим более детально – досудебное урегулирование. Для начала – необходимо детально изучить заключенный между
    туроператором и Вами Договор, он может содержать важные условия расторжения , а также возврата денежных средств.
    <br/>
    <br/>
    Далее, необходимо обратиться к Туроператору с Заявлением о расторжении Договора и возврате денежных средств. Данное
    заявление может быть направлено как напрямую к Туроператору, так и через Турагенство.
    <br/>
    <br/>
    Согласно <a href="http://www.consultant.ru/document/cons_doc_LAW_12462/b3d98f4dc10414887863cc0ee0dfd636b644b25b/"
                style="color: #0000ff;" target="_blank" rel="noopener">ст. 14 Федеральный закон от 24.11.1996 N 132-ФЗ
        “Об основах туристской деятельности в Российской Федерации” </a> В случае возникновения обстоятельств,
    свидетельствующих о возникновении в стране (месте) временного пребывания туристов (экскурсантов) угрозы безопасности
    их жизни и здоровья, а равно опасности причинения вреда их имуществу, турист (экскурсант) и (или) туроператор
    (турагент) вправе потребовать в судебном порядке расторжения договора о реализации туристского продукта или его
    изменения.
    <br/>
    <br/>
    Наличие указанных обстоятельств подтверждается соответствующими решениями (рекомендациями) федеральных органов
    государственной власти, органов государственной власти субъектов Российской Федерации, органов местного
    самоуправления, принимаемыми в соответствии с федеральными законами.
    <br/>
    <br/>
    При расторжении до начала путешествия договора о реализации туристского продукта в связи с наступлением
    обстоятельств, указанных в настоящей статье, туристу и (или) иному заказчику возвращается денежная сумма, равная
    общей цене туристского продукта, а после начала путешествия – ее часть в размере, пропорциональном стоимости не
    оказанных туристу услуг.
