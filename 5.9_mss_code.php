        в момент обращения в эквайер банк!
    </div>
    <br/>
    Если же одна торговая точка занимается не одним видом деятельности, а сразу несколькими, банк, скорее
    всего, присвоит ей МСС код по основному виду.
    <br/>
    <br/>
    Или же предприятие заключает договор с несколькими банками и у каждой точки на предприятии будет свой
    МСС код. Также, один банк может предоставить клиенту (торгово-сервисному предприятию) сразу несколько
    POS-терминалов (это терминал для оплаты банковскими картами) с разными MCC кодами.
    <br/>
    <br/>
    Например: в ресторане, который находится в гостинице, скорее всего, будет присвоен МСС 5814 (Фастфуд)
    или 5812 (Места общественного питания, рестораны), а не 7011 (Отели и мотели – нигде более не
    классифицированные).
