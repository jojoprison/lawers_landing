    <br/>
    MCC код звучит как Merchant Category Code. В переводе на русский – «Код категории продавца». Данный код
    состоит из четырех цифр, он обозначает сферу деятельности ТСП (торгово сервисного предприятия), то есть
    какими услугами или товарами она занимается, так же тип операции (покупка или снятие денег, так же,
    перевод).
    <br/>
    <br/>
    Например код 5411: Бакалейные магазины, супермаркеты
    <br/>
    <br/>
    <div class="img" style="text-align:center;">
        <img src="https://charge-backer.ru/wp-content/uploads/2020/07/image_2020-07-15_13-33-23.png"
