    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        В чем выгода банка, когда большой процент возврата.
    </h3>
    Встречается такое, что банк предлагает вам высокий кэшбэк на определенные покупки. Но это будет на
    небольшой период (информация может быть прописана на сайте банка или в условиях обслуживания), что бы у
    вас выработалась привычка пользоваться картой этого банка.
    <br/>
    <br/>
    Но чаще всего этот процент выплачивает предприятие, организация, у которой вы совершили покупку, а ваш
    банк выступает в роли посредника.
    <br/>
    <br/>
    Как это происходит: Вы оплачиваете покупку – Банк продавца (банк-эквайер) определяет МСС код и присылает
    его в Ваш Банк (банк-эммитент) – Банк-эммитент получает вознаграждение, процент за привлеченного клиента
    и начисляет вам cash-back, он может быть в рублях, в бонусах, в милях и так далее.
