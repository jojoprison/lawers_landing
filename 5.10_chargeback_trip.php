    <br/>
    В сложившейся ситуации введении карантина по причине распространении Covid-19 – актуальным вопросом стал – возврат
    денежных средств за путевку.
    <br/>
    <br/>
    Так, возврат денежных средств за несостоявшееся путешествие, казалось бы, обычная практика, которая предусматривает
    собой либо досудебное урегулирование вопроса, либо судебное разбирательство. Однако, все мы понимает, что судебные
    тяжбы очень долгие, а также нужно отметить – дорогостоящие. С большой вероятностью, Турагент может предложить
    перенести тур и Вы можете согласиться, однако если это Вас не устраивает, смело требуйте возврат денежных средств.
    <br/>
    <br/>

    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
