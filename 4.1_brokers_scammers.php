    <h3 style="color:green;-webkit-text-stroke: 0.2px black">А кто же такой РЕАЛЬНЫЙ брокер?</h3><br/>
    Брокер на рынке ценных бумаг -- профессиональный участник, имеющий право совершать операции по
    поручениям Клиентов на покупку или продажу активов. Для предоставления услуг на территории РФ брокер
    должен обладать лицензией.
    <br/><br/>

    <h3 style="color:green;-webkit-text-stroke: 0.2px black">Лицензия РЕАЛЬНОГО брокера</h3><br/>
    Лицензия брокера -- это документ, который подтверждает профессионализм и дает право заниматься
    финансовой деятельностью на фондовом рынке.
    Брокерская фирма имеет возможность владеть следующими видами лицензией:
    <br/><br/>
    <ul>
        <li>На ведение брокерской деятельности;</li>
        <li>На ведение дилерской деятельности;</li>
        <li>На работа по управлению ценными бумагами на фондовом рынке.</li>
    </ul>
    <br/>
    На территории РФ выдать лицензию на осуществление брокерской деятельности может только ЦБ РФ,
    ознакомиться со списком брокеров, имеющих такую лицензию Вы можете по ссылке: <a
            href="https://cbr.ru/securities_market/registries/">https://cbr.ru/securities_market/registries/</a>
