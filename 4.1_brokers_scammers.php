    соотношение между заёмным и собственным капиталом). Кредитное плечо предлагающее соотношение выше, чем
    1:50, является подозрительным. Кредитное плечо более 1:10 опасно для неопытного трейдера. Оно
    предоставляет возможным открывать сделки критичного объем и соответственно нести крупные потери, как и
    возможно крупного выигрыша.
