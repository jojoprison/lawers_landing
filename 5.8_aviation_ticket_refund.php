    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Схема действий для возврата денежных средств за авиабилет
    </h3>
    При отсутствии результата можно действительно отправить письмо в Росавиацию и приложить переписку с перевозчиком. В
    большинстве случаев после обращения авиаперевозчик возвращает деньги. Но время рассмотрения жалобы в Росавиации
    может затянуться.
    <br/>
    <br/>
    Если самостоятельно получить возврат не удалось, придется обращаться в суд. В этом случае понадобится помощь юриста.
    Любой юрист согласится вести дело, так как споры между пассажиром и авиаперевозчиком в большинстве случае
    заканчиваются в пользу пассажира. Каждый юрист понимает, что в ходе судебного процесса можно добиться выплаты пени и
    штрафов с ответчика.
