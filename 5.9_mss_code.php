    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Расскажем для чего нужен МСС код и почему необходимо его знать.
    </h3>
    Данный код присваивают к каждой покупке, которая совершалась по банковской карте для того, что бы
    определить, подходит Ваша покупка под программу лояльности «cash-back» или нет.
    <br/>
    <br/>
    Так же, как уже говорилось ранее, МСС код применяется и при снятии наличных, денежных переводах, а так
    же, при пополнении карты. Благодаря MCC коду ваш банк определяет, какие конкретно операции проводятся по
    карте и какую необходимо удержать с вас комиссию.
    <br/>
    <br/>
    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Дебетовая или кредитная карта с кэшбэком (cash-back).
    </h3>
    Cash-back – в переводе на русский – это возврат наличных денег. То есть, это возврат определенного
    процента от суммы покупки за товар или услугу, на которую у вас подключена услуга cash-back. Так же,
    есть карты, которые делают возврат на все покупки, какая именно программа лояльности подключена у вас,
    можно уточнить в своем банке, позвонив на горячую линию или написать в чат.
    <br/>
