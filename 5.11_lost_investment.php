    <br/>
    <br/>
    3) Минимальный начальный капитал поможет принести немалую прибыль на которой вы можете, расширится или открыть филиалы под собственным управлением;
    <br/>
    <br/>
    4) Возможность влиять на работу предприятия;
    <br/>
    <br/>
    5) Пассивный доход;
