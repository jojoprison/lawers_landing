                НСПК “МИР” позволяет эмитентам инициировать диспут при условии:
                «Товар не получен»
                Причина запуска БП Диспут «Товар не получен» применяется, когда Держатель карты понес финансовые потери
                по одной из следующих причин:
                • ТСП не доставило товар в согласованный срок и, если применимо, в согласованное место;
                • ТСП не оказало услуги в согласованный срок;
                • ТСП не выполнило работы в согласованный срок;
                • ТСП не сможет выполнить свои обязательства в связи с прекращением
                деятельности.
                Если дата доставки товаров (оказания услуг, выполнения работ) не была согласована, Эмитент использует
                дату, предполагаемую Держателем карты.
                Эмитенту следует рекомендовать Держателю карты попытаться предварительно урегулировать диспут с ТСП
