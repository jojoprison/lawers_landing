    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Розыгрыши от мошенников
    </h3>
    Как правило, в сообществе вконтакет/инстаграм/одноклассники создается пост о розыгрыше. Далее Вам поступает личное
    сообщение с поздравлением: Вы получаете главный приз. Организатор готов отправить его, только есть одно условие:
    доставка за Ваш счет. Как правило, «победитель» и не задумывается, что может быть подвох, но ситуация повторяется --
    никакой доставки не будет, а оплата за нее уже осела в кармане мошенников.
