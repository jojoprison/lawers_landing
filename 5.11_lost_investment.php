    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Банковские вклады
    </h3>
    Банковский вклад (депозит) это денежные вложения переданные банку под проценты с условием возврата. Банковский вклад
    может совершить гражданин РФ достигший 14-летнего возврата. Банк обязан заключить с вкладчиком договор с
    прописанными условиями возврата вклада, при чем условия могут быть прописаны любые, но не противоречащих закону РФ.
    Так должен быть прописан срок вклада, если вкладчик не снимает свой вклад по истечению срока, договор считается
    пролонгированным на тех же условиях, если иное не прописано в договоре.
    <br/>
    <br/>
    Так же банковский вклад может быть открыт и на третье лицо по доверенности или же на несовершеннолетнего ребенка где
    обязательно прописано условие, что ребенок может воспользоваться вкладом при наступлении совершеннолетия. Банковский
    вклад может быть унаследован по наследству или по закону. Для заключения договора о Банковском вкладе достаточно
    предъявить просто паспорт. Максимальная сумма страхового возмещения по вкладам составляет 1,4 миллиона рублей.
    Другими словами, государство обязуется выплатить эти деньги в случае отзыва лицензии у Банка.
