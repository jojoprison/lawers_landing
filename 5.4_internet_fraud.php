    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Благотворительность
    </h3>
    Создается большое количество постов, сообществ в социальных сетях, сайтов, направленных «на помощь нуждающимся», где
    описываются тяжелые судьбы людей, а зачастую используют и детей. После прочтения, например, истории болезни малыша,
    мало кто пожалеет перевести хотя бы 100 рублей, к сожалению эти деньги могут не поступить в нужные руки, а история
    окажется выдуманной.
