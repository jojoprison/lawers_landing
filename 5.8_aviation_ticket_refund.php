    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Куда жаловаться если не возвращают деньги
    </h3>
    Представители авиакомпаний прилагают все усилия, чтобы не выплачивать деньги при отказе пассажира совершать полет.
    Они могут аргументировать отказ внутренними правилами, указаниями, приказами и другими соответствующими документами.
    Их действия направлены для достижения одной цели. Как можно меньшую сумму от первоначальной цены вам возвратить.
    <br/>
    <br/>
    Если письменное обращение и личные переговоры не привели к положительному результату, то тогда можно смело подавать
    жалобу:
    <br/>
    <br/>
    1) В Федеральное агентство воздушного транспорта. Не обязательно писать заявление и отправлять по почте или лично
    приносить. Жалобу можно отправить через интернет, посетив сайт <a href="https://favt.ru" target="_blank"
                                                                      rel="noopener">Росавиации</a>.
    <br/>
    2) Дополнительно можно обратиться в <a href="http://souz-potrebiteley.ru" target="_blank" rel="noopener">Союз
        потребителей</a>.
    <br/>
    3) Написать жалобу в <a href="https://www.rospotrebnadzor.ru" target="_blank" rel="noopener">Роспотребнадзор</a>.
    <br/>
    <br/>
    В большинстве случаев подобные обращения оказывают положительное влияние на разрешение конфликта.
