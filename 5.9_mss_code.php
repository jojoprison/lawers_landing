    <br/>
    Это магазины «магнит», «пятерочка», «лента» и так далее.
    <br/>
    <br/>
    Когда вы покупаете хлеб в «магните» по этой операции будет указан код 5411.
    <br/>
    <br/>
    MCC код назначается банком, предоставляющим услугу эквайринга (ссылка, что такое экваринг
    https://ru.wikipedia.org/wiki/Эквайринг). То есть когда определенная организация, которая принимает
    оплату банковской картой, заключает договор эквайринга (банк начинает обслуживать
    организацию/предприятие), Банк назначает ей код исходя из того, какой вид деятельности у предприятия.
    <br/>
    <br/>
