    <h3 style="color:green;-webkit-text-stroke: 0.2px black">
        Три вида оплат юридических услуг
    </h3>
    1. Полная оплата услуги.
    Этот вариант удобен для разовых услуг. Например, для подготовки договора, составления документа на
    расторжение договорных отношений, регистрации ИП или ООО.
    <br/>
    <br/>
    2. Предоплата + премия за успех.
    В данном случаи объем услуг обговаривается перед заключением договора и дополнительных оплат. Другие
    оплаты не предусмотрены. Такой способ мотивирует обе стороны сразу. Клиент, оплативший первую часть, не
    опустит руки на полпути, а юрист/компания проведут качественную работу для получения премии за успех.
    <br/>
    <br/>
    3. Поэтапная оплата услуг.
    Клиенту нужно будет оплачивать каждое действие юриста/компании, будь то консультация, составление
    претензии или подготовка правовой позиции. В итоге набирается приличная сумма, зачастую превышающая
    первоначальные ожидания.
