    Также стоит обратить внимание на ст. 6 Федеральный закон от 24.11.1996 N 132-ФЗ “Об основах туристской деятельности
    в Российской Федерации” При подготовке к путешествию, во время его совершения, включая транзит, турист имеет право
    на возмещение убытков и компенсацию морального вреда в случае невыполнения условий договора о реализации туристского
    продукта туроператором или турагентом в порядке, установленном законодательством Российской Федерации;
    <br/>
    <br/>
    Таким образом, все ответственность ложиться полностью на Туроператора.
